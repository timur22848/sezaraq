// Function to handle the smooth scroll
function scrollToElement(event) {
  event.preventDefault(); // Prevent the default anchor link behavior
  var targetId = event.currentTarget.getAttribute('href');
  var targetPosition = document.querySelector(targetId).offsetTop; // Get the top position of the target element

  window.scrollTo({ // Perform the scroll
    top: targetPosition,
    behavior: 'smooth' // Smooth scroll
  });
}

// Add an event listener to your link
document.getElementById('scrollLink').addEventListener('click', scrollToElement);
